export function getCategoryOrder(operation_type) {
  const base = ['corporate', 'legal', 'financial', 'product', 'hr', 'commercial']
  if (operation_type === 'captacao') return [...base, 'captacao_specific']
  if (operation_type === 'ma') return [...base, 'ma_specific']
  return base
}

export function getItemsByCategory(category_id, operation_type) {
  return CHECKLIST_ITEMS.filter(
    item => item.category === category_id &&
      (item.flow === 'both' || item.flow === operation_type)
  )
}
